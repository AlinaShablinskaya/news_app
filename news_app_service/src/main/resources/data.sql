(
    id SERIAL,
    date timestamp,
    text character varying NOT NULL,
    username character varying NOT NULL,
    id_news bigint,
    PRIMARY KEY (id),
    FOREIGN KEY (id_news) REFERENCES news ON DELETE SET NULL
);

